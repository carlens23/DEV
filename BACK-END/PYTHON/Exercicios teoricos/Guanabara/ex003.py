a = input("Digite algo: ")
print(f"O tipo primitivo de {a}, {type(a)}")
print(f"È composto de somente espaços?, {a.isspace()}")
print(f"È Numerico?, {a.isnumeric()}")
print(f"È alfabético?, {a.isalpha()}")
print(f"È alfanumŕico?, {a.isalnum()}")
print(f"È maíusculo?, {a.isupper()}")
print(f"È mínusculo?, {a.islower()}")
print(f"Esta capitalizado?, {a.istitle()}")


